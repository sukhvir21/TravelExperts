
public class Agent {

// Declare Variables
	private int Agentid;
	private String FirstName;
	private String LastName;
	private String MiddleInitials;
	private String AgtBusiness;
	private String Email;
	private String AgentPosition;
	private int AgencyId;
	
	//Getter & Setter Methods for all values
	public int getAgentid() {
		return Agentid;
	}
	public void setAgentid(int agentid) {
		Agentid = agentid;
	}
	public String getFirstName() {
		return FirstName;
	}
	public void setFirstName(String firstName) {
		FirstName = firstName;
	}
	public String getLastName() {
		return LastName;
	}
	public void setLastName(String lastName) {
		LastName = lastName;
	}
	public String getMiddleInitials() {
		return MiddleInitials;
	}
	public void setMiddleInitials(String middleInitials) {
		MiddleInitials = middleInitials;
	}
	public String getAgtBusiness() {
		return AgtBusiness;
	}
	public void setAgtBusiness(String agtBusiness) {
		AgtBusiness = agtBusiness;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public String getAgentPosition() {
		return AgentPosition;
	}
	public void setAgentPosition(String agentPosition) {
		AgentPosition = agentPosition;
	}
	public int getAgencyId() {
		return AgencyId;
	}
	public void setAgencyId(int agencyId) {
		AgencyId = agencyId;
	}
	
}
